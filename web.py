#!/usr/bin/env python3
"""RadioResurrector Web Interface"""

import json
import os
import secrets
import subprocess

from flask import Flask, jsonify, redirect, render_template_string, request, session, url_for

BASE_DIR = "/opt/radio"
CONFIG_FILE = os.path.join(BASE_DIR, "config.json")
CONFIG_SH = os.path.join(BASE_DIR, "config.sh")
BUFFER_DIR = os.path.join(BASE_DIR, "buffer")

app = Flask(__name__)

DEFAULT_CONFIG = {
    "secret_key": secrets.token_hex(32),
    "pin": "1234",
    "stream_url": "https://findyourownstream.example/stream",
    "chunk_seconds": 300,
    "buffer_minutes": 120,
    "check_interval": 10,
    "volume": 90,
    "streams": [
        {"name": "Example Stream", "url": "https://findyourownstream.example/stream"}
    ],
}


def load_config():
    if not os.path.exists(CONFIG_FILE):
        save_config(DEFAULT_CONFIG)
        return DEFAULT_CONFIG
    with open(CONFIG_FILE) as f:
        return json.load(f)


def save_config(cfg):
    with open(CONFIG_FILE, "w") as f:
        json.dump(cfg, f, indent=2)
    write_shell_config(cfg)


def write_shell_config(cfg):
    with open(CONFIG_SH, "w") as f:
        f.write("# Auto-generated by RadioResurrector web interface — do not edit manually\n")
        f.write(f'STREAM_URL="{cfg["stream_url"]}"\n')
        f.write(f'CHUNK_SECONDS={cfg["chunk_seconds"]}\n')
        f.write(f'BUFFER_MINUTES={cfg["buffer_minutes"]}\n')
        f.write(f'MAX_CHUNKS=$(( BUFFER_MINUTES * 60 / CHUNK_SECONDS ))\n')
        f.write(f'CHECK_INTERVAL={cfg["check_interval"]}\n')
        f.write(f'VOLUME={cfg["volume"]}\n')


def service_status():
    try:
        r = subprocess.run(
            ["systemctl", "is-active", "radio.service"],
            capture_output=True, text=True, timeout=5,
        )
        return r.stdout.strip()
    except Exception:
        return "unknown"


def buffer_chunk_count():
    try:
        return len([f for f in os.listdir(BUFFER_DIR) if f.endswith(".mp3")])
    except Exception:
        return 0


cfg_boot = load_config()
app.secret_key = cfg_boot.get("secret_key", secrets.token_hex(32))


